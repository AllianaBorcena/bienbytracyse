    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request) : RedirectResponse
    {
        $request->validate([
            'name' => ['required', 'max:255'],
            'price' => ['required', 'numeric'],
            'product_id' => ['required', 'integer']
        ],[
            'name.required'=> 'Product option name is required',
            'name.max' => 'Product Option Max Length is 255',
            'price.required' => 'Product Price Max Length is required',
            'price.numeric' => 'Product Price have to be a number',

        ]);

        $option = new ProductOption();
        $option->product_id = $request->product_id;
        $option->name = $request->name;
        $option->price = $request->price;
        $option->save();

        toastr()->success('Created Successfully');

        return redirect()->back();

    }

